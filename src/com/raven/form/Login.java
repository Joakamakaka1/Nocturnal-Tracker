    private void handleFailedLogin(boolean userExists) {
        if (!userExists) {
            setPlaceholder(txtUser, "Usuario no existe", true);
        } else {
            txtPassword.setEchoChar((char) 0);
            setPlaceholder(txtPassword, "Contraseña incorrecta", true);
        }

    }

    private void setPlaceholder(JTextField field, String message, boolean isError) {
        field.setText("");
        field.setForeground(Color.RED);
        field.setText(message);
        field.addFocusListener(new FocusAdapter() {
            @Override
            public void focusGained(FocusEvent e) {
                if (field.getForeground().equals(Color.RED)) {
                    field.setText("");
                    field.setForeground(Color.BLACK);
                    txtPassword.setEchoChar((char) 8226);
                }
            }
        });
    }

