    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String inputUser = txtUser.getText();
        String inputPassword = new String(txtPassword.getPassword());

        ClienteController cc = new ClienteController();
        ArrayList<Cliente> clientes = cc.listarUsuarios();

        boolean userExists = false;
        boolean loginSuccessful = false;

        for (Cliente cliente : clientes) {
            if (cliente.getUsuario().equals(inputUser)) {
                userExists = true;
                if (cliente.getPassword().equals(inputPassword)) {
                    loginSuccessful = true;
                    clienteId = cliente.getId();
                    System.out.println("Cliente ID asignada: " + clienteId);
                    break;
                }
            }
        }

        if (loginSuccessful) {
            JOptionPane.showMessageDialog(this, "Login exitoso con el usuario " + inputUser);
            Main m = new Main();
            m.setVisible(true);
            this.dispose();
        } else {
            handleFailedLogin(userExists);
        }
    }//GEN-LAST:event_btnLoginActionPerformed

